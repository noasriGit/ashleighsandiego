/**
 * Generate public/idx-search-bar.js for the IDX wrapper header.
 * Keeps area/zip config in sync with src/data/community-zips.ts.
 *
 * Usage: node scripts/generate-idx-search-bar.mjs
 */

import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const LAUNCH_SLUGS = [
  "la-jolla",
  "pacific-beach",
  "university-city",
  "clairemont",
  "mission-valley",
  "del-mar",
  "carmel-valley",
  "point-loma",
  "sorrento-valley",
  "bay-park",
  "ocean-beach",
  "hillcrest",
  "north-park",
];

const communitiesSource = readFileSync(join(root, "src/data/communities.ts"), "utf8");
const communityNames = {};
for (const match of communitiesSource.matchAll(/slug:\s*"([^"]+)"[\s\S]*?name:\s*"([^"]+)"/g)) {
  communityNames[match[1]] = match[2];
}

const zipsSource = readFileSync(join(root, "src/data/community-zips.ts"), "utf8");
const zipBlock = zipsSource.match(/const ZIP_OVERRIDES[\s\S]*?= \{([\s\S]*?)\};/)?.[1];
if (!zipBlock) {
  console.error("Could not parse ZIP_OVERRIDES from community-zips.ts");
  process.exit(1);
}

const zipOverrides = {};
for (const match of zipBlock.matchAll(/(?:"([^"]+)"|([a-z0-9_-]+)):\s*\[([^\]]*)\]/g)) {
  const slug = match[1] ?? match[2];
  const zips = [...match[3].matchAll(/"(\d{5})"/g)].map((m) => m[1]);
  zipOverrides[slug] = zips;
}

const overridesPath = join(root, "data/idx-search-overrides.json");
let overrides = {};
try {
  overrides = JSON.parse(readFileSync(overridesPath, "utf8"));
} catch {
  console.warn("No idx-search-overrides.json found; saved search URLs omitted from IDX search bar.");
}

const areas = [{ slug: "_general", label: "All San Diego", zipCodes: zipOverrides._general ?? [], savedSearchUrl: overrides._general?.savedSearchUrl ?? "" }];
for (const slug of LAUNCH_SLUGS) {
  areas.push({
    slug,
    label: communityNames[slug] ?? slug,
    zipCodes: zipOverrides[slug] ?? [],
    savedSearchUrl: overrides[slug]?.savedSearchUrl ?? "",
  });
}

const output = `/**
 * IDX wrapper search bar — generated by scripts/generate-idx-search-bar.mjs
 * Do not edit by hand; run npm run idx:generate-search-bar after changing areas or prices.
 */
(function () {
  var IDX_BASE_URL = "https://sdcommunities.idxbroker.com";
  var IDX_MLS_ID = "d010";
  var IDX_RESULTS_PATH = "/idx/results/listings";

  var AREAS = ${JSON.stringify(areas, null, 2)};

  var BUY_THRESHOLDS = [500000, 750000, 1000000, 1250000, 1500000, 2000000, 2500000, 3000000, 5000000];
  var RENT_THRESHOLDS = [1500, 2000, 2500, 3000, 3500, 4000, 5000, 6000, 7500, 10000];

  function formatBuyPrice(amount) {
    if (amount >= 1000000) {
      var millions = amount / 1000000;
      return Number.isInteger(millions) ? "$" + millions + "M" : "$" + millions.toFixed(2).replace(/\\.?0+$/, "") + "M";
    }
    return "$" + Math.round(amount / 1000) + "K";
  }

  function formatRentPrice(amount) {
    return "$" + amount.toLocaleString("en-US") + "/mo";
  }

  function buildPriceOptions(thresholds, format, emptyLabel) {
    var options = [{ value: "", label: emptyLabel }];
    thresholds.forEach(function (amount) {
      options.push({ value: String(amount), label: format(amount) });
    });
    return options;
  }

  function fillSelect(select, options) {
    select.innerHTML = "";
    options.forEach(function (option) {
      var el = document.createElement("option");
      el.value = option.value;
      el.textContent = option.label;
      select.appendChild(el);
    });
  }

  function getAreaConfig(slug) {
    for (var i = 0; i < AREAS.length; i++) {
      if (AREAS[i].slug === slug) return AREAS[i];
    }
    return AREAS[0];
  }

  function buildSearchUrl(filters) {
    var area = getAreaConfig(filters.areaSlug);
    var hasGeo = area.zipCodes.length > 0;
    var hasFilters =
      filters.minPrice != null ||
      filters.maxPrice != null ||
      filters.minBed != null ||
      filters.propertyType === "rnt";

    if (!hasFilters) {
      if (hasGeo) {
        var geoParams = new URLSearchParams();
        geoParams.set("idxID", IDX_MLS_ID);
        area.zipCodes.forEach(function (zip) {
          geoParams.append("zipcode[]", zip);
        });
        geoParams.append("a_status[]", "active");
        return IDX_BASE_URL + IDX_RESULTS_PATH + "?" + geoParams.toString();
      }
      if (area.savedSearchUrl) return area.savedSearchUrl;
      return IDX_BASE_URL;
    }

    var params = new URLSearchParams();
    params.set("idxID", IDX_MLS_ID);
    area.zipCodes.forEach(function (zip) {
      params.append("zipcode[]", zip);
    });
    if (filters.minPrice != null) params.set("lp", String(filters.minPrice));
    if (filters.maxPrice != null) params.set("hp", String(filters.maxPrice));
    if (filters.minBed != null) params.set("bd", String(filters.minBed));
    if (filters.propertyType) params.set("pt", filters.propertyType);
    params.append("a_status[]", "active");

    return IDX_BASE_URL + IDX_RESULTS_PATH + "?" + params.toString();
  }

  function parseAmount(value) {
    if (!value) return undefined;
    var amount = Number(value);
    return Number.isFinite(amount) ? amount : undefined;
  }

  function initSearchBar(root) {
    var listingType = "buy";
    var typeButtons = root.querySelectorAll("[data-listing-type]");
    var areaSelect = root.querySelector("[data-area]");
    var minSelect = root.querySelector("[data-min-price]");
    var maxSelect = root.querySelector("[data-max-price]");
    var bedSelect = root.querySelector("[data-beds]");
    var form = root.querySelector("form");

    function syncPriceOptions() {
      var thresholds = listingType === "rent" ? RENT_THRESHOLDS : BUY_THRESHOLDS;
      var format = listingType === "rent" ? formatRentPrice : formatBuyPrice;
      fillSelect(minSelect, buildPriceOptions(thresholds, format, "No min"));
      fillSelect(maxSelect, buildPriceOptions(thresholds, format, "No max"));
    }

    typeButtons.forEach(function (button) {
      button.addEventListener("click", function () {
        listingType = button.getAttribute("data-listing-type") || "buy";
        typeButtons.forEach(function (btn) {
          var active = btn === button;
          btn.setAttribute("aria-pressed", active ? "true" : "false");
          btn.classList.toggle("is-active", active);
        });
        minSelect.value = "";
        maxSelect.value = "";
        syncPriceOptions();
      });
    });

    syncPriceOptions();

    form.addEventListener("submit", function (event) {
      event.preventDefault();
      var min = parseAmount(minSelect.value);
      var max = parseAmount(maxSelect.value);
      if (min != null && max != null && min > max) {
        var swap = min;
        min = max;
        max = swap;
      }
      var url = buildSearchUrl({
        areaSlug: areaSelect.value,
        minPrice: min,
        maxPrice: max,
        minBed: bedSelect.value ? Number(bedSelect.value) : undefined,
        propertyType: listingType === "rent" ? "rnt" : undefined,
      });
      window.location.href = url;
    });
  }

  document.querySelectorAll("[data-sd-search-bar]").forEach(initSearchBar);
})();
`;

const outPath = join(root, "public/idx-search-bar.js");
writeFileSync(outPath, output, "utf8");
console.log("Wrote " + outPath);
