/**
 * Generate tier-2/3 living-guide keyword lists from community-seed-keywords.csv.
 * Also emits keywordMappings snippets for src/data/keywords.ts.
 *
 * Usage: node scripts/generate-community-keywords.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const csvPath = join(root, "data", "community-seed-keywords.csv");

function parseCsvLine(line) {
  const values = [];
  let current = "";
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === '"') {
      inQuotes = !inQuotes;
      continue;
    }
    if (ch === "," && !inQuotes) {
      values.push(current);
      current = "";
      continue;
    }
    current += ch;
  }
  values.push(current);
  return values;
}

function parseCsv(text) {
  const lines = text.replace(/^\uFEFF/, "").trim().split(/\r?\n/);
  const headers = parseCsvLine(lines[0]);
  return lines.slice(1).map((line) => {
    const values = parseCsvLine(line);
    return Object.fromEntries(headers.map((h, i) => [h.trim(), (values[i] ?? "").trim()]));
  });
}

const PHASE1_SLUGS = new Set([
  "bay-ho", "north-clairemont", "kearny-mesa", "linda-vista", "mission-hills",
  "torrey-pines", "del-mar-heights", "torrey-hills", "civita", "serra-mesa",
  "la-jolla-cove", "la-jolla-shores", "la-jolla-village", "bird-rock", "muirlands",
  "mount-soledad", "windansea", "crown-point", "mission-beach", "mission-bay",
  "morena", "old-town", "midway-district", "point-loma-heights", "university-heights",
  "normal-heights", "bankers-hill", "little-italy", "downtown-san-diego", "balboa-park",
]);

const rows = parseCsv(readFileSync(csvPath, "utf8"));
const living = rows.filter(
  (r) => r.page_type === "living" && PHASE1_SLUGS.has(r.slug),
);

const LONG_TAIL = [
  "is {area} good for families",
  "{area} vs {parent} san diego",
  "what is it like to live in {area}",
  "pros and cons of living in {area}",
  "should i move to {area} san diego",
  "{area} commute to ucsd",
  "{area} commute to downtown san diego",
  "{area} condos vs single family",
  "walkability in {area} san diego",
  "school districts in {area} san diego",
  "relocating to {area} san diego",
  "buying a home in {area} guide",
  "{area} neighborhood for remote workers",
  "{area} san diego buyer guide",
  "where to live in {area} san diego",
];

const mappings = [];

for (const row of living) {
  const tier = row.tier;
  const outDir = join(root, "data", "keywords", `tier-${tier}`);
  mkdirSync(outDir, { recursive: true });

  const seeds = (row.seed_keywords ?? "")
    .split(";")
    .map((s) => s.trim().toLowerCase())
    .filter(Boolean);

  if (seeds.length === 0) {
    console.warn(`skip ${row.slug}: no seed keywords`);
    continue;
  }

  const area = row.name.toLowerCase();
  const parent = (row.parent_slug || "").replace(/-/g, " ");
  const expanded = new Set(seeds);

  for (const tpl of LONG_TAIL) {
    expanded.add(
      tpl.replace(/\{area\}/g, area).replace(/\{parent\}/g, parent || area),
    );
  }

  if (parent) {
    expanded.add(`${area} vs ${parent}`);
    expanded.add(`living in ${area} or ${parent}`);
  }

  const keywords = [...expanded].filter((k) => k.length > 3 && k.length < 120);
  const outPath = join(outDir, `${row.slug}.txt`);
  writeFileSync(outPath, keywords.join("\n"), "utf8");
  console.log(`${row.slug}: ${keywords.length} keywords -> ${outPath}`);

  const page = `/neighborhoods/${row.slug}`;
  for (const kw of seeds.slice(0, 12)) {
    mappings.push({ keyword: kw, page, intent: "community-guide" });
  }
}

const lines = mappings.map(
  (m) =>
    `  { keyword: ${JSON.stringify(m.keyword)}, page: ${JSON.stringify(m.page)}, intent: "community-guide" },`,
);

const tsOut = join(root, "src", "data", "community-guide-keywords.generated.ts");
writeFileSync(
  tsOut,
  `import type { KeywordMapping } from "./keywords-types";\n\n/** Auto-generated by npm run keywords:generate-community */\nexport const phase1GuideKeywords: KeywordMapping[] = [\n${lines.join("\n")}\n];\n`,
);
console.log(`\nWrote ${mappings.length} mappings -> ${tsOut}`);
